package org.example.DiagramasClases.Diagrama2;

public class Fecha {
    private int dia;
    private int mes;
    private int anyo;

    public Fecha(int dia, int mes, int anyo) {
        this.dia = dia;
        this.mes = mes;
        this.anyo = anyo;
    }
    public Fecha(int mes, int anyo) {
        this.mes = mes;
        this.anyo = anyo;
    }

}